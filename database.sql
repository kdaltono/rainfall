DROP TABLE IF EXISTS MONTHS;
CREATE TABLE MONTHS(
	MonthID			INT(2) NOT NULL CHECK(MonthID > 0),
	MonthName		VARCHAR(10) NOT NULL,
	MonthDayCount	INT(2) NOT NULL CHECK(MonthDayCount < 32),
	PRIMARY KEY(MonthID)
);

DROP TABLE IF EXISTS RAINFALL;
CREATE TABLE RAINFALL(
	RainfallID		INT(5) NOT NULL AUTO_INCREMENT,
	MonthID			INT(2) REFERENCES MONTHS(MonthID),
	RainfallVal		NUMERIC(7,2) NOT NULL,
	YearID			INT(4) REFERENCES YEARS(YearID),
	PRIMARY KEY(RainfallID)
);

DROP TABLE IF EXISTS YEARS;
CREATE TABLE YEARS(
	YearID			INT(4) NOT NULL,
	PRIMARY KEY(YearID)
);

